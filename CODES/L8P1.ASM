.MODEL SMALL
.STACK 100H   
.DATA
MSG1 DB "ENTER A HEX DIGIT: $"
MSG2 DB 10,13,"IN BINARY IT IS: $" 
.CODE   
MAIN PROC  
MOV AX, @DATA
MOV DS, AX

MOV AH,9
LEA DX,MSG1
INT 21H
        
MOV AH, 1
INT 21H

MOV AH, 9
LEA DX, MSG2
INT 21H   

CMP AL, '9'
JLE NUMBER
SUB AL, 55
JMP CONVERTED
        
NUMBER:
SUB AL, 48           
   
CONVERTED:
MOV BL,AL
MOV CX,8  
   
PRINT_:
ROL BL, 1
JC  DISPONE
MOV DL, '0' 
JMP DISPLAY
        
DISPONE:
MOV DL, '1' 
DISPLAY:
MOV AH, 2
INT 21H
LOOP PRINT_

EXIT:  
MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN