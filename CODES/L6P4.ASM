.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "ENTER A LINE: $"
MSG2 DB 10,13,"NUMBER OF CHARACTERS ARE: $"
VAR DB 0
.CODE 
MAIN  PROC
MOV AX,@DATA
MOV DS,AX

MOV AH,9
LEA DX,MSG1
INT 21h

WHILE_:
MOV AH,1
INT 21H

CMP AL,13
JE END_WHILE

INC VAR

JMP WHILE_

END_WHILE:
MOV AH,9
LEA DX,MSG2
INT 21H
                        
MOV BL,VAR
ADD BL,48 
MOV DL,BL
MOV AH,2
INT 21H

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
