.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "ENTER A STRING: $"
MSG2 DB 10,13,"REVERSE STRING: $"
.CODE
MAIN PROC
MOV AX, @DATA
MOV DS, AX   
     
MOV AH, 9
LEA DX, MSG1
INT 21H 

MOV CX, 0 ;TO COUNT THE CHARACTER

MOV AH, 1
INPUT:
INT 21H

CMP AL, 13
JE END_INPUT

PUSH AX
INC CX
JMP INPUT        

END_INPUT:
MOV AH, 9
LEA DX, MSG2
INT 21H        

MOV AH, 2
PRINT:
POP DX
INT 21H
LOOP PRINT        

MOV AH, 4CH
INT 21H  
      
MAIN ENDP
END MAIN