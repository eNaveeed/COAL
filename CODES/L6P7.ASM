.MODEL SMALL
.STACK 100H
.DATA       
MS1 DB 10,13,"ENTER A HEX DIGIT:$"
MS2 DB 10,13,"IN DECIMAL IT IS: $"
MS5 DB 10,13,"IN DECIMAL IT IS: 1$"
MS3 DB 10,13,"DO YOU WANT TO DO IT AGAIN?: $"
MS4 DB 10,13,"ILLEGAL CHARACTER - ENTER 0 ... 9 OR A ... F$"
COT DB 0
.CODE 
MAIN  PROC 
MOV AX,@DATA
MOV DS,AX

ST_:            
MOV AH,9 
LEA DX,MS1         
INT 21H

MOV AH,1
INT 21H
MOV BL,AL

CMP  BL,'9'
JLE DIGIT

CMP BL,'A'
JGE CHAR
JMP ERROR

DIGIT:  
MOV AH,9 
LEA DX,MS2         
INT 21H

MOV AH,2
MOV DL,BL
INT 21H
JMP EXIT

CHAR: 
CMP BL,'G' 
JGE ERROR

SUB BL,17
MOV AH,9 
LEA DX,MS5         
INT 21H

MOV AH,2
MOV DL,BL 
INT 21H
JMP EXIT

ERROR:
MOV AH,9 
LEA DX,MS4         
INT 21H

MOV DL,COT 
CMP DL,3
JE END_
INC DL
MOV COT,DL
JMP ST_

EXIT:
MOV AH,9 
LEA DX,MS3         
INT 21H

MOV AH,1
INT 21H
MOV CL,AL

CMP CL,'Y'
JE ST_
CMP CL,'y'
JE ST_

JMP END_ 

END_:         
MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
