.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB "ENTER BINARY: $"
MSG2 DB 10,13,"YOU ENTERED: $"
MSG3 DB 10,13,"INVALID BINARY$"
.CODE
MAIN PROC
MOV AX, @DATA
MOV DS, AX

MOV AH, 9
LEA DX, MSG1
INT 21H

XOR BL, BL         
MOV CX, 8   
     
READ_BINARY:
MOV AH, 1 
INT 21H

CMP AL, '0'        
JB INVALID_INPUT 
CMP AL, '1'
JA INVALID_INPUT 
  
SHL BL, 1
SUB Al, '0'
OR BL, AL 
LOOP READ_BINARY
         
MOV AH, 9
LEA DX, MSG2
INT 21H

MOV CX, 8
PRINT:
ROL BL, 1
JC ONE
MOV DL,'0'
MOV AH, 2
INT 21H

LOOP PRINT
JMP EXIT
ONE:
 MOV DL,'1'
 MOV AH, 2
 INT 21H
 LOOP PRINT

INVALID_INPUT:
MOV AH, 9
LEA DX, MSG3
INT 21H

EXIT:
MOV AH, 4CH
INT 21H

MAIN ENDP
END MAIN