.MODEL SMALL
.STACK 100H
.DATA
.CODE 
MAIN  PROC
MOV AH,2 
MOV BL,80H         
MOV CL,10
JMP WHILE_

AC:
MOV CL,10

MOV DL,10
INT 21H
MOV DL,13
INT 21H
JMP WHILE_

WHILE_:
MOV DL,BL
INT 21H 
INC BL

CMP BL,0FFh 
JE END_WHILE

DEC CL 
CMP CL,0
JE AC

JMP WHILE_

END_WHILE:       
MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN

